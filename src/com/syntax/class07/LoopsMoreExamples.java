package com.syntax.class07;

public class LoopsMoreExamples {

	public static void main(String[] args) {
		
		/*
		 * print numbers from 10 to 100;
		 */
		
		int a=10;
	while(a<=100){
		System.out.print(a+" ");
		a++;
	}
	// print numbers from 100 to 50;
	
	int b=100;
	while (b>=50) {
		
		System.out.print(b+" ");
		b--;
	}
	// print numbers from 1 to 20 but only even numbers
	System.out.println();
	}

}
